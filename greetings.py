username = input("Hi, what is your name? ")
  # username is a symbol representing a placeholder in memory (VARIABLE)
  # input() is a FUNCTION that collects user input
  # the output of the input() function is ASSIGNED to the variable username
print("Hi, " + str(username) + "!")
  # print() is a PRINT function in Python
  # str() is a function in Python that converts its input into a STRING
  # + is a symbol for string concatenation
